#include <stdio.h>

int main(void){
    int n = 0x8FA6002D;
    printf("%d , %d, %#X\n",9 & 5, -9 & 5, n & 0xFFFF);
    /*
     1) 9 & 5
        0000 0000 0000 0000 0000 0000 0000 1001
      & 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        0000 0000 0000 0000 0000 0000 0000 0001 = 1
     2) -9 & 5
        1111 1111 1111 1111 1111 1111 1111 0111
      & 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        0000 0000 0000 0000 0000 0000 0000 0101 = 5
     3) 0x8FA6002D & 0xffff
        1000 1111 1010 0110 0000 0000 0010 1101
      & 0000 0000 0000 0000 1111 1111 1111 1111
        ---------------------------------------
        0000 0000 0000 0000 0000 0000 0010 1101 = 45 = 0x2D
     */

    n = 0x2D;
    printf("%d , %d, %#X\n",9 | 5, -9 | 5, n | 0xFFFF0000);
    /*
     1) 9 | 5
        0000 0000 0000 0000 0000 0000 0000 1001
      | 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        0000 0000 0000 0000 0000 0000 0000 1101 = 13
     2) -9 | 5
        1111 1111 1111 1111 1111 1111 1111 0111
      | 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        1111 1111 1111 1111 1111 1111 1111 0111 to 
        1000 0000 0000 0000 0000 0000 0000 1001 = -9
     3) 0x2D | 0xffff0000, 功能：高16位置1，低16位保留
        0000 0000 0000 0000 0000 0000 0010 1101
      | 1111 1111 1111 1111 0000 0000 0000 0000
        ---------------------------------------
        1111 1111 1111 1111 0000 0000 0010 1101 = 0xffff002D
     */

    n = 0x0A07002D;
    printf("%d , %d, %#X\n",9 ^ 5, -9 ^ 5, n ^ 0xFFFF0000);
    /*
     1) 9 ^ 5
        0000 0000 0000 0000 0000 0000 0000 1001
      | 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        0000 0000 0000 0000 0000 0000 0000 1100 = 12
     2) -9 ^ 5
        1111 1111 1111 1111 1111 1111 1111 0111
      | 0000 0000 0000 0000 0000 0000 0000 0101
        ---------------------------------------
        1111 1111 1111 1111 1111 1111 1111 0010 to 
        1000 0000 0000 0000 0000 0000 0000 1110 = -14
     3) 0x0A07002D ^ 0xffff0000, 功能：高16位反转，低16位保留
        0000 1010 0000 0111 0000 0000 0010 1101
      | 1111 1111 1111 1111 0000 0000 0000 0000
        ---------------------------------------
        1111 0101 1111 1000 0000 0000 0010 1101 = 0xf5f8002D
     */

    printf("%d, %d\n",~9, ~-9);
    /*
     1) ~9
        ~ 0000 0000 0000 0000 0000 0000 0000 1001
        ---------------------------------------
          1111 1111 1111 1111 1111 1111 1111 0110 to
          1000 0000 0000 0000 0000 0000 0000 1010 = -10
     2) ~-9
        ~ 1111 1111 1111 1111 1111 1111 1111 0111
        ---------------------------------------
          0000 0000 0000 0000 0000 0000 0000 1000 = 8
     */
    return 0;
}
